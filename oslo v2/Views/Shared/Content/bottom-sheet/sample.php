<div class="content">
	A bottom sheet is a sheet of material that slides up from the bottom edge of the screen and presents a set of clear and simple actions.
	<h3>Usage</h3>
	Bottom sheets are best suited to displaying three or more actions that do not require a description. If there are fewer than three actions, or a detailed description is required, consider using a menu or dialog instead.<br><br>
	You can use bottom sheets to show actions related to other apps, or to allow entry points to other apps (via the app icon).<br><br>
	Bottom sheets can be list-style or grid-style. Grid layouts are easier to scan.
